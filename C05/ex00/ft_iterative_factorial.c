int	ft_iterative_factorial(int nb)
{
	int	s;

	s = 1;
	if (nb < 0)
		return (0);
	if (nb == 0 || nb == 1)
		return (1);
	while (nb > 0)
	{
		s *= nb;
		nb--;
	}
	return (s);
}
