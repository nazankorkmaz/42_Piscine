int	ft_is_prime(int nb)
{
	int	i;
	int	k;

	k = nb / 2;
	i = 2;
	if (nb <= 1)
		return (0);
	while (i <= k)
	{
		if (nb % i == 0)
			return (0);
		i++;
	}
	return (1);
}
